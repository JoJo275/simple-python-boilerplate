def _discover_all_scripts() -> list[str]:
    """Dynamically discover all deletable scripts in scripts/.

    Returns paths for all scripts except shared modules
    (_colors.py, _imports.py, _ui.py, _progress.py,
    _container_common.py, _doctor_common.py) and customize.py.

    Returns:
        List of relative paths (files and subdirectories).
    """
    scripts_dir = ROOT / "scripts"
    paths: list[str] = []
    if not scripts_dir.is_dir():
        return paths
    for entry in sorted(scripts_dir.iterdir()):
        if entry.name.startswith("."):
            continue
        rel = f"scripts/{entry.name}"
        if entry.is_dir():
            if entry.name not in {"__pycache__"}:
                paths.append(f"{rel}/")
        elif entry.is_file() and entry.name not in _SHARED_MODULES:
            paths.append(rel)
    return paths


def _discover_src_content_files() -> list[str]:
    """Dynamically discover deletable source files in src/<package>/.

    Returns paths for all source files except __init__.py,
    _version.py, and py.typed.

    Returns:
        List of relative paths.
    """
    # Try current package name, fall back to template
    pkg_dir = ROOT / "src" / TEMPLATE_PACKAGE_NAME
    if not pkg_dir.is_dir():
        # Check for any subdirectory in src/
        src_dir = ROOT / "src"
        if not src_dir.is_dir():
            return []
        subdirs = [
            d for d in src_dir.iterdir() if d.is_dir() and d.name != "__pycache__"
        ]
        if not subdirs:
            return []
        pkg_dir = subdirs[0]

    preserve = {"__init__.py", "_version.py", "py.typed"}
    paths: list[str] = []
    pkg_rel = f"src/{pkg_dir.name}"

    for entry in sorted(pkg_dir.iterdir()):
        if entry.name in preserve or entry.name == "__pycache__":
            continue
        rel = f"{pkg_rel}/{entry.name}"
        if entry.is_dir():
            paths.append(f"{rel}/")
        elif entry.is_file():
            paths.append(rel)
    return paths


def _discover_code_workspace_files() -> list[str]:
    """Dynamically discover .code-workspace files at the repo root.

    Returns:
        List of relative paths for any .code-workspace files found.
    """
    return [
        entry.name
        for entry in sorted(ROOT.iterdir())
        if entry.is_file() and entry.suffix == ".code-workspace"
    ]


def strip_directories(
    keys: list[str],
    *,
    dry_run: bool = False,
    strippable: dict[str, dict[str, object]] | None = None,
    files_only_keys: set[str] | None = None,
) -> list[str]:
    """Remove optional directories and files from the project.

    Separates files and directories, showing what will be removed
    with color-coded output and per-group labels.  After all removals,
    checks for newly-empty parent directories and offers to remove them.

    Args:
        keys: Keys from *strippable* identifying what to remove.
        dry_run: If ``True``, report without deleting.
        strippable: Registry to look up keys.  Defaults to :data:`STRIPPABLE`.
        files_only_keys: Keys for which only files should be deleted
            (directory structures are preserved).

    Returns:
        Relative paths that were removed (or would be removed).
    """
    if strippable is None:
        strippable = STRIPPABLE
    files_only = files_only_keys or set()

    c = Colors()
    sym = unicode_symbols()
    removed: list[str] = []
    # Track parent directories whose children were deleted so we can
    # offer to remove them if they become empty.
    affected_parents: set[Path] = set()

    for key in keys:
        entry = strippable.get(key)
        if entry is None:
            continue
        label = str(entry.get("label", key))
        paths: list[str] = entry["paths"]  # type: ignore[assignment]

        # Dynamic path discovery for special entries with empty paths
        if key == "all-scripts" and not paths:
            paths = _discover_all_scripts()
        elif key == "src-content" and not paths:
            paths = _discover_src_content_files()
        elif key == "code-workspace" and not paths:
            paths = _discover_code_workspace_files()

        is_files_only = key in files_only

        # Partition into existing dirs and files
        dirs: list[tuple[str, Path]] = []
        files: list[tuple[str, Path]] = []
        for rel_path in paths:
            target = ROOT / rel_path
            if not target.exists():
                continue
            if target.is_dir():
                dirs.append((rel_path, target))
            else:
                files.append((rel_path, target))

        if not dirs and not files:
            continue

        mode_hint = " (files only)" if is_files_only else ""
        print(f"\n  {c.bold(c.cyan(f'[{key}]'))} {c.dim(label)}{c.dim(mode_hint)}")

        for rel_path, target in dirs:
            if is_files_only:
                # Delete files inside the directory but keep the directory
                dir_files = [p for p in target.rglob("*") if p.is_file()]
                for f in dir_files:
                    f_rel = f.relative_to(ROOT).as_posix()
                    removed.append(f_rel)
                    if dry_run:
                        print(
                            f"    {c.yellow(sym['arrow'])} Would remove file: {c.dim(f_rel)}"
                        )
                    else:
                        try:
                            f.unlink()
                            print(f"    {c.green(sym['check'])} Removed file: {f_rel}")
                        except (OSError, PermissionError) as exc:
                            log.warning(
                                "    %s Failed to remove %s: %s",
                                sym["cross"],
                                f_rel,
                                exc,
                            )
            else:
                removed.append(rel_path)
                if dry_run:
                    print(
                        f"    {c.yellow(sym['arrow'])} Would remove directory: {c.yellow(rel_path)}"
                    )
                else:
                    try:
                        shutil.rmtree(target)
                        print(
                            f"    {c.green(sym['check'])} Removed directory: {rel_path}"
                        )
                        affected_parents.add(target.parent)
                    except (OSError, PermissionError) as exc:
                        log.warning(
                            "    %s Failed to remove %s: %s",
                            sym["cross"],
                            rel_path,
                            exc,
                        )

        for rel_path, target in files:
            removed.append(rel_path)
            if dry_run:
                print(
                    f"    {c.yellow(sym['arrow'])} Would remove file: {c.dim(rel_path)}"
                )
            else:
                try:
                    target.unlink()
                    print(f"    {c.green(sym['check'])} Removed file: {rel_path}")
                    affected_parents.add(target.parent)
                except (OSError, PermissionError) as exc:
                    log.warning(
                        "    %s Failed to remove %s: %s", sym["cross"], rel_path, exc
                    )

    # Offer to clean up empty parent directories (skip if files_only for all)
    if not all(k in files_only for k in keys):
        _cleanup_empty_parents(affected_parents, dry_run=dry_run)

    return removed


def _cleanup_empty_parents(
    parents: set[Path],
    *,
    dry_run: bool = False,
) -> None:
    """Check affected parent directories and remove them if empty.

    Walks up toward ROOT (but never beyond it), removing directories
    that are now empty after file/subdirectory stripping.

    Args:
        parents: Set of parent directory paths to check.
        dry_run: If ``True``, report without deleting.
    """
    c = Colors()
    sym = unicode_symbols()
    cleaned: set[Path] = set()

    for parent in sorted(parents):
        current = parent
        while current != ROOT and current not in cleaned:
            if not current.is_dir():
                break
            # A directory is "empty" if it has no children at all
            children = list(current.iterdir())
            if children:
                break
            rel = current.relative_to(ROOT)
            if dry_run:
                print(
                    f"    {c.yellow(sym['arrow'])} Would remove empty directory: {c.dim(str(rel) + '/')}"
                )
            else:
                try:
                    current.rmdir()
                    print(
                        f"    {c.green(sym['check'])} Removed empty directory: {rel!s}/"
                    )
                except OSError:
                    break
            cleaned.add(current)
            current = current.parent


